"""
3. Сформировать из введенного числа обратное по порядку входящих в него цифр и вывести на экран.
   Например, если введено число 3486, надо вывести 6843.
"""

number = int(input('Введите многозначное натуральное число: '))

new_number = ''

for k in str(number):
    new_number = k + new_number

new_number = int(new_number)  # для вывода результата это действие не нужно, но по условию требуется получить число ))
print(f"{number} -> {new_number}")
